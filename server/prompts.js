// Системные промпты и JSON-схемы для извлечения данных моделью Qwen3-VL.
// Распознавание идёт ПОСТРАНИЧНО: на каждый шаг — одна фотография и своя схема.

const BASE_RULES = `Ты — система извлечения данных из документов. На вход — фото одной страницы документа.
Извлеки данные строго в JSON по заданной схеме. Правила:
- Возвращай ТОЛЬКО валидный JSON, без markdown, без пояснений.
- Бери значения с документа, НЕ транслитерируй с латиницы, НЕ переводи сам.
- ВАЖНО: документы Республики Беларусь ДВУЯЗЫЧНЫЕ — значения напечатаны как
  «белорусское / русское» через косую черту. Для договора ВСЕГДА бери РУССКИЙ
  вариант (часть ПОСЛЕ «/»). Русский вариант не содержит букв «і», «ў» и слов
  Рэспубліка/вобласць/раён. Пример: «КІРЫЛ / КИРИЛЛ» → «КИРИЛЛ».
- РЕГИСТР: ФИО, орган выдачи, адрес возвращай в обычном регистре (с заглавной —
  имена собственные и первое слово), НЕ ЗАГЛАВНЫМИ целиком. Пример:
  «ШТЕПА КИРИЛЛ ИГОРЕВИЧ» → «Штепа Кирилл Игоревич». Аббревиатуры (РОВД, РУВД,
  ОВД) оставляй заглавными. Коды и номера — точно как на документе.
- Если значение нечитаемо или отсутствует — ставь "" и добавь имя поля
  в low_confidence_fields. Ничего не выдумывай. Пустое поле лучше выдуманного.
- Даты — в формате ДД.ММ.ГГГГ.`

// Конфигурация по типу страницы: схема + специфические правила.
const DOCS = {
  // 1) Страница с фотографией и MRZ
  passport_main: {
    schema: { identification_number: '', passport_number: '', date_of_issue: '', low_confidence_fields: [] },
    rules: `
- Это страница паспорта РБ с фотографией и машиночитаемой зоной (MRZ — строки с «<<<»).
- identification_number — идентификационный (личный) номer, 14 знаков (буквы и цифры).
- passport_number — номер паспорта: две латинские буквы + 7 цифр (напр. «MC2864848»).
- date_of_issue — дата выдачи. Если есть MRZ — сверь номер паспорта и даты по ней.`,
  },

  // 2) Разворот с ФИО на кириллице
  passport_names: {
    schema: { surname_ru: '', name_ru: '', patronymic_ru: '', issuing_authority_ru: '', low_confidence_fields: [] },
    rules: `
- Это разворот паспорта РБ с ФИО на кириллице и сведениями о выдаче.
- Фамилию, имя, отчество бери РУССКИМ вариантом (после «/»), в обычном регистре.
- issuing_authority_ru возвращай в ТВОРИТЕЛЬНОМ падеже (отвечает на «Выдан КЕМ?»):
  НЕ «Слуцкий РОВД» (именительный), а «Слуцким РОВД Минской области».
  Примеры: «Партизанским РУВД г. Минска», «Министерством внутренних дел Республики Беларусь».`,
  },

  // 3) Страница с пропиской (штампы регистрации)
  passport_reg: {
    schema: { registration_address_ru: '', low_confidence_fields: [] },
    rules: `
- Это страница паспорта РБ со штампами регистрации (прописки).
- Если штампов несколько — бери ТОЛЬКО САМЫЙ НИЖНИЙ (с самой поздней датой). Верхние игнорируй.
- Формат: область, [район — только если он напечатан в нижнем штампе], населённый пункт,
  улица, дом. Если в штампе город («г.») как районный центр — район НЕ пиши.
  Пример: «Минская область, г. Слуцк, ул. Урожайная, д. 3».
- Название улицы, населённого пункта и НОМЕР ДОМА обычно ВПИСАНЫ ОТ РУКИ.
  Читай ровно те символы, что написаны, и НИЧЕГО НЕ ДОБАВЛЯЙ: не дописывай нули,
  не округляй. Если написано «3» — это «д. 3», а НЕ «д. 30».
- РЯДОМ с номером дома часто стоит ДАТА регистрации — НЕ склеивай цифры дома с
  цифрами даты. Если дом «5», а рядом дата «01.1997» — номер дома «5», а не «501».
- Если рукописный текст или номер дома не читаются уверенно — оставь их пустыми
  и добавь "registration_address_ru" в low_confidence_fields. Не выдумывай.`,
  },

  // 4) СРТС — лицевая сторона (характеристики ТС)
  vehicle_front: {
    schema: {
      reg_number: '', make_model: '', year: '', color_ru: '', vin: '',
      vehicle_type_ru: '', body_type_ru: '', max_mass_kg: '', unladen_mass_kg: '',
      certificate_number: '', low_confidence_fields: [],
    },
    rules: `
- Это ЛИЦЕВАЯ сторона свидетельства о регистрации ТС (СРТС / техпаспорт):
  характеристики автомобиля.
- VIN (поле «Кузаў (рама)») : ровно 17 символов, латиница+цифры, без I/O/Q.
  Не сходится — занеси "vin" в low_confidence_fields.
- certificate_number — номер свидетельства (напр. «3ТМ0028520»).
- Тип ТС, тип кузова, марку и модель, цвет возвращай ЗАГЛАВНЫМИ буквами, как на бланке
  («ЛЕГКОВОЙ АВТОМОБИЛЬ», «СЕДАН (SEDAN)», «HYUNDAI SANTAFE», «БЕЛЫЙ»).`,
  },

  // 5) СРТС — оборотная сторона (владелец + адрес)
  vehicle_back: {
    schema: { surname_ru: '', name_ru: '', patronymic_ru: '', registration_address_ru: '', certificate_date: '', low_confidence_fields: [] },
    rules: `
- Это ОБОРОТНАЯ сторона свидетельства о регистрации ТС: владелец (УЛАСНІК) и его адрес (АДРАС).
- Из блока УЛАСНІК (владелец) возьми ФИО: surname_ru, name_ru, patronymic_ru — РУССКИЙ
  вариант (кириллица, не латиница), в обычном регистре. Часто указаны и фамилия, и имя,
  и отчество (напр. «РУДЕНКО НИКОЛАЙ ВИКТОРОВИЧ»).
- certificate_date — «Дата выдачы» (дата выдачи свидетельства) в формате ДД.ММ.ГГГГ.
- registration_address_ru собери из полей: Вобласць (область), Раён (район), Нас. пункт
  (населённый пункт), Вуліца (улица), Дом. Формат: «область, [район], населённый пункт,
  улица, дом». Если населённый пункт — город («г.») и является районным центром, район можно
  опустить. Пример: «Гомельская область, г. Речица, ул. Достоевского, д. 5».
- Бери РУССКИЙ вариант значений. НОМЕР ДОМА — ровно те цифры, что напечатаны; ничего не
  добавляй и не округляй. Нечитаемое — оставь пустым и добавь "registration_address_ru" в
  low_confidence_fields.`,
  },
}

// Авто-режим: модель сама определяет тип страницы и заполняет только нужные поля.
