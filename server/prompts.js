export const DOC_TYPES = Object.keys(DOCS)

// Системный промпт для конкретного типа страницы: база + правила + схема.
export function buildPrompt(docType) {
  const cfg = DOCS[docType]
  if (!cfg) throw new Error(`Неизвестный тип документа: ${docType}`)
  return `${BASE_RULES}${cfg.rules}\nСхема ответа:\n` + JSON.stringify(cfg.schema, null, 2)
}
